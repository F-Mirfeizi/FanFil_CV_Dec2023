import math


# Basic Calculator

def add(num_1, num_2):
    return num_1 + num_2

def sub(num_1, num_2):
    return num_1 - num_2

def multiple(num_1, num_2):
    return num_1*num_2


def division(num_1, num_2):
    return num_1 / num_2


#Scientific Calculator

def logarithm(num_1, num_2):
    m = math.log(num_1, num_2)
    return m 

def power(num_1, num_2):
    return math.pow(num_1, num_2)


    
